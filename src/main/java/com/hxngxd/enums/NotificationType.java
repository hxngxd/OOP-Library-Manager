package com.hxngxd.enums;

/**
 * Enum NotificationType đại diện cho các loại thông báo.
 */
public enum NotificationType {
    REMINDER,      // Nhắc nhở (ví dụ: sắp đến hạn trả sách).
    WARNING,       // Cảnh báo (ví dụ: vi phạm quy định).
    INFORMATION,   // Thông tin (ví dụ: sách mới được thêm).
    ALERT          // Cảnh báo hệ thống.
}